x = input('Digite algo:')
print('a informação digitada é um num interio?', x.isnumeric())
print('a informação digitada é alfabetica?', x.isalpha())
print('a informação digitada é alfanumerica?', x.isalnum())
print('a informação digitada é um espaço?', x.isspace())
print('a informação digitada esta em letras min?', x.islower())
print('a informação digitada esta em letras max?', x.isupper())
print('a informação digitada esta capitalizada?', x.istitle())

